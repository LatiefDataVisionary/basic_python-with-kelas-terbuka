print("\n# Dictionary (dict) ---> Associative Array")
# bila di List kita menggunakan index
# maka bila di Dictionary, kita menggunakan Identifier
# Identifier ---> key

data_dict = {
    'key1':'value1',
    'key2':'value2'
}

print(data_dict)

data_dict = {
    'key':'value',
    'cp':'Ucup',
    'tg':'Otong',
    'dg':'Dudung',
    'nmbr':100,
    'list':data_list,
}

# data_dict bisa jg dimasukin boolean, dan bahkan dictionary yg lain

print(data_dict)
print(data_dict["cp"])
print(data_dict["tg"])
print(data_dict["nmbr"])
print(data_dict["list"])


